Proiectul intitulat „Managementul Lanțului de Policlinici Apollo” 
are scopul de a simula un sistem de management pentru un lanț de policlinici
 din mai multe orașe(Baia Mare, Baia Sprie, Cluj-Napoca, Bucureşti şi Haţeg), 
fiecare având: 
•	program specific, 
•	tipuri diferite de cabinete, 
•	tipuri diferite de angajați cu grad şi specializări 
•	echipamente specifice fiecărui cabinet
Fiecare tip de angajat deține un cont personal de logare, 
iar în funcție de postul pe care îl ocupă, are acces la diferite 
operațiuni în funcţie de un rol şi permisiuni: realizarea programărilor, 
vizualizarea datelor financiare cu privire la unitatea medicală în care lucrează, 
vizualizarea sau editarea unor date despre angajații policlinicii, 
chiar şi editarea datelor personale . 


Baza de date este formata din 5 fisiere text atasate in cod_sql.rar:
-tabele
-triggere
-proceduri
-populare
-views

se da run si se pot implementa interogari.

La partea de interfata grafica codul este atasat in cod_java.rar
se creeaza un proiect nou, se foloseste o locatie cu fisier cu numele Clinica, 
unde se adauga fisierele din rar.

se modifica root-ul si parola din baza de date corespunzatoare si se da run la proiect.
astfel, ca si in documentatie se pot vedea pasii pentru aplicatie, butoane

Enjoy !



